package com.microsoft.test.annotations;

public interface UnitTest {
  /*
   * This empty interface should be used to categorize
   * unit tests using the JUnit 4.x @Cateogory annotation.
   * 
   * 
   * A unit test tests a single unit in the system. Ideally 
   * a unit test should test
   * one-and-only class, and it should 
   * not have any external input or output.
   * Do not use this category if the test 
   * interacts with a database(not even fixtures), network,
   * or file system.
   * 
   */
}
